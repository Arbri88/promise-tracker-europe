export function getCountryBySlug(slug) {
  return EUROPE_COUNTRIES.find(c => c.slug === slug) || null;
}

export function normalizeCountrySlugFromName(name) {
  const hit = EUROPE_COUNTRIES.find(c => c.name.toLowerCase() === String(name || '').toLowerCase());
  return hit?.slug || String(name || '').trim().toLowerCase();
}

// Returns the "current" actor for a country slug if present, otherwise null.
export function getCurrentActorForCountry(slug) {
  const matches = ACTORS.filter(a => a.slug === slug);
  if (!matches.length) return null;
  const current = matches.find(a => a.isCurrent);
  return current || matches[0];
}

export function getPromisesForCountry(slug) {
  // Country slug is derived from promise.country (name) if not present
  return PROMISES.filter(p => normalizeCountrySlugFromName(p.country) === slug);
}

export function getEvidenceForPromise(promiseId) {
  return EVIDENCE.filter(e => e.promiseId === promiseId);
}
